import java.util.Scanner;

public class Solution {
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int a = in.nextInt();
		double b = in.nextDouble();
		in.nextLine();
		String s = in.nextLine();
		System.out.println("String: " + s + "\nDouble: " + b + "\nInt: " + a);
	}
}
